// Типы формы результата EvalResult + образец mock-данных.
// Соответствует методологии Агента 01: 14 критериев в 5 группах.

/** Группы критериев (цвета в theme.css: --gA … --gE). */
export type CriterionGroup = 'A' | 'B' | 'C' | 'D' | 'E';

/** Идентификаторы 14 критериев. */
export type CriterionId =
  | 'A1' | 'A2' | 'A3' | 'A4'
  | 'B1' | 'B2' | 'B3' | 'B4'
  | 'C1' | 'C2' | 'C3'
  | 'D1'
  | 'E1' | 'E2';

export type ConfidenceLevel = 'высокая' | 'средняя' | 'низкая';

export interface Criterion {
  /** Короткое название критерия (display-вариант из рубрики). */
  title: string;
  /** Балл 0–10. */
  score: number;
  /** Обоснование оценки. */
  rationale: string;
  /** Уверенность в оценке. */
  confidence: ConfidenceLevel;
  /** Источник/основание (URL или текст), если есть. */
  source?: string;
  /** Группа критерия. */
  group: CriterionGroup;
  /**
   * Критерий с инвертированной шкалой в методике: показывается
   * display-название, при котором «выше = лучше». Помечается в UI.
   */
  inverted: boolean;
}

export interface Verdict {
  /** Ярлык вердикта: «Требует изменений» и т.п. */
  label: string;
  /** Рекомендованное действие. */
  action: string;
}

export interface GroupMeta {
  id: CriterionGroup;
  title: string;
}

export interface EvalResult {
  /** Формулировка идеи/гипотезы. */
  idea: string;
  /** Итоговый балл 0–10. */
  score_final: number;
  verdict: Verdict;
  /** Режим оценки: экспресс / глубокий (как отдаёт бэк). */
  mode: 'express' | 'deep';
  /** Общая уверенность разбора. */
  confidence: ConfidenceLevel;
  /** Критерии по идентификаторам (все 14). */
  criteria: Record<CriterionId, Criterion>;
  /** Красная команда — главные риски. */
  red_team: string[];
  /** Источники, на которых основана оценка. */
  evidence_sources: EvidenceSource[];
  /**
   * Почему балл срезан: человеческие формулировки сработавших
   * киллер-ограничений методики (пусто — если ничего не сработало).
   */
  killers: string[];
  /** Допущения разбора: чего не хватило в данных и как это учтено. */
  assumptions: string[];
}

export interface EvidenceSource {
  title: string;
  url?: string;
}

